#List in python
#List is a collection which is ordered and changeable. Allows duplicate members.

#Creating a List
thislist = ["apple", "banana", "cherry","pineapple","orange","grapes"]
print(thislist)
print(thislist[0])
print(thislist[0][2])
print(thislist[1:])
print(thislist[1:4])
print(thislist[-1])
print(type(thislist))
print(len(thislist))
name = 'strawberry'
print(type(name))
print(thislist + [name])




